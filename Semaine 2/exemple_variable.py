# Exemples de différente variable / types / formule / fonction


age = 44
print("variable age", type(age))

pi = 3.14
print("variable pi", type(pi))

question = "comment ca va"
print("variable question", type(question))

condition = True
print("variable condition", type(condition))

une_list = [1, "lilia", 5.12]
print("variable list", type(une_list))

une_turple = (1, "salut", False)
print("variable turple", type(une_turple))

un_dict = {
    "prénom": "lilia",
    "nom": "hocine",
    "age": 44
    }
print("variable un_dict", type(un_dict))

un_set = {14, 18, "quelque chose", -89, True}
print("variable un_set", type(un_set))

