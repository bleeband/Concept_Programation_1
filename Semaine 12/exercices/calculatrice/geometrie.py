
def aire_carre(cote):
    """Retourne l'aire d'un carré de côté `cote`."""
    return cote * cote

def aire_rectangle(longueur, largeur):
    """Retourne l'aire d'un rectangle de longueur `longueur` et de largeur `largeur`."""
    return longueur * largeur

def aire_cercle(rayon):
    """Retourne l'aire d'un cercle de rayon `rayon`."""
    pi = 3.1416
    return pi * rayon ** 2